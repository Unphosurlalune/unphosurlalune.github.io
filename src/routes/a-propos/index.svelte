<h2>Deux qui s’aiment…</h2>
<p>
Nous sommes un couple marié depuis plus de 10 ans. Le secret? Malheureusement beaucoup de disputes et parfois tres dure… mais s’aimer c’est plus que emotion c’est une chose qui vaut la peine de se battre pour elle.<br>
Quel joie de tenir encore dans notre couple !<br>
Nous sommes plutôt discrets mais foufou dans l’intimité. On a plein d’idées, de rêve et on souhaite vous en partager un peut sur se blog.
</p>

<h2>UnPho quoi?</h2>
<p>Un pho sur la lune se prononce info sur la lune.
Le Pho est une spécialité vietnamienne et un délice ! C’est une soupe trop bonne.
Du coup un pho sur la lune c’est notre blog… notre soupe idée de pensé que l’on partage ici!
C’est notre soupe… agrémentée par qui nous sommes, par nos valeurs….
</p>
<h2>Des valeurs</h2>
<p>C’est si triste qu’aujourd’hui nous parlons de valeurs et non plus de ce qui est moral.
Car les valeurs changent, évoluent (lire régressent) alors que ce qui est moral ne change pas….
Alors nos valeur a nous ce sont celles de Dieu, celui décris dans la bible celui des hébreux… le seul, le vrai.
Très longtemps nous avons eu honte et caché notre foi, notre croyance (souvent sous le couvert de les gens ne veulent pas savoir et ca nous arrange)… mais aujourd’hui nous n’avons plus le temps d’avoir honte… car le plus beau trésor pour nous serait que vos coeurs soient touchés et que vous choisissez et vivez votre propre expérience avec lui…
</p>
<p><i>Attention, il n’y a plus le temps… c’est maintenant…</i></p>

<p>Nos valeurs ce sont donc, l’amour, la bienveillance, la justice (de Dieu pas des hommes).
Nous ne sommes pas des saints et pour nous être chrétiens signifie seulement chercher à être meilleur! Nous ne nous considérons pas supérieur mais juste chanceux de le connaître et pouvoir s’appuyer sur lui dans ce monde si dur….
</p>

<h2>Pas de jugement</h2>
<p>Si parfois dans nos propos vous venez a vous sentir jugé, sachet que NON, nous ne vous jugeons pas! Nous somme navrée si cela arrive mais cela est sûrement lié aux valeurs  que l’on partage ou non. Personne n’est meilleur et personne n’est pire. Nous prenons seulement se bonne ou mauvaise decision et ce nest que si mous prenons de constamment et sciemment des mauvaise décision que nous sommes une mauvaise personne.</p>

<h2>Pas de commentaire</h2>
<p>A l’heure actuelle nous ne permettons pas les commentaires sur notre blog. C’est comme ça c’est notre blog.</p>


